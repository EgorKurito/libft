int	ft_isdigit(int c)
{
	return (c >= '0' && c <= '9');
}

int	is_char_digit(char *str)
{
	int	r;

	r = 0;
	while (str[r])
	{
		if (str[r] < '0' || str[r] > '9')
			return (0);
		r++;
	}
	return (1);
}
